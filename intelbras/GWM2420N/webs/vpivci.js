var curprovider = 0;
var curprovince = 0;
var curcity = 0;

function initVpiVci()
{
	var f = document.forms[0];
	f.province.disabled = false;
	f.city.disabled = false;
	f.zone.disabled = false;
	initProvider();
}

function initProvider()
{
	var f = document.forms[0];
	
	f.provider.options.length = 0;  
	for(var i=0;i<vpi_vci.length;i++)   
	{     
		//var newoption = document.createElement("OPTION");
		//newoption.text= vpi_vci[i][0];
		//f.provider.appendChild(newoption);
		f.provider.options[f.provider.length] = new Option(vpi_vci[i][0], i, false, false);
	}
	curprovider = 0;
	initProvince();
}

function initProvince()
{
	var f = document.forms[0];
	
	f.province.options.length = 0;
	for(var i=1;i<vpi_vci[curprovider].length;i++)   
	{   
		f.province.options[f.province.length] = new Option(vpi_vci[curprovider][i][0], i-1, false, false);
	}
	curprovince = 0;
	initCity();
}

function initCity()
{
	var f = document.forms[0];
	
	f.city.options.length = 0;
	for(var i=1;i<vpi_vci[curprovider][curprovince+1].length;i++)   
	{   
		f.city.options[f.city.length] = new Option(vpi_vci[curprovider][curprovince+1][i][0], i-1, false, false);
	}
	curcity = 0;
	initZone();
}

function initZone()
{
	var f = document.forms[0];
	
	f.zone.options.length = 0;
	for(var i=1;i<vpi_vci[curprovider][curprovince+1][curcity+1].length;i++)   
	{   
		f.zone.options[f.zone.length] = new Option(vpi_vci[curprovider][curprovince+1][curcity+1][i][0], i-1, false, false);
	}
	onZoneChange(0);
}

function onProviderChange(idx)
{
	var f = document.forms[0];
	curprovider = idx;
	if((idx+1) == vpi_vci.length)
	{
		f.province.disabled = true;
		f.city.disabled = true;
		f.zone.disabled = true;	
		document.getElementById("vpivci").style.display = "block";
	}else
	{
		f.province.disabled = false;
		f.city.disabled = false;
		f.zone.disabled = false;
		document.getElementById("vpivci").style.display = "none";
	}
	initProvince();
	onProvinceChange(0);
	onCityChange(0);
}
function onProvinceChange(idx)
{
	curprovince = 0;
	initCity();
}
function onCityChange(idx)
{
	curcity = 0;
	initZone();
}
function onZoneChange(idx)
{
	var f= document.forms[0];
	f.province.disabled = false;
	f.city.disabled = false;
	//f.zone.disabled = false;
	if(f.provider.selectedIndex==0)
	{
		f.province.disabled = true;
		f.city.disabled = true;
		f.zone.disabled = true;	
	}else if(f.province.selectedIndex==0)
	{
		f.city.disabled = true;
		//f.zone.disabled = true;
		if((f.provider.selectedIndex+1)==vpi_vci.length)
			f.province.disabled = true;
	}else if(f.city.selectedIndex==0)
	{
		f.zone.disabled = false;		
	}
	
	f.atmVpi.value = "";
	f.atmVci.value = "";
	if(vpi_vci[curprovider][curprovince+1][curcity+1][idx+1][1]!="")
		f.atmVpi.value = vpi_vci[curprovider][curprovince+1][curcity+1][idx+1][1];
	if(vpi_vci[curprovider][curprovince+1][curcity+1][idx+1][2]!="")
		f.atmVci.value = vpi_vci[curprovider][curprovince+1][curcity+1][idx+1][2];
	document.getElementById("selectVpi").innerHTML=vpi_vci[curprovider][curprovince+1][curcity+1][idx+1][1];
	document.getElementById("selectVci").innerHTML=vpi_vci[curprovider][curprovince+1][curcity+1][idx+1][2];
}